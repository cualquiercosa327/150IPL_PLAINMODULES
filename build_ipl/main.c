void ErrorExit(char *fmt, ...)
{
	va_list list;
	char msg[256];	

	va_start(list, fmt);
	vsprintf(msg, fmt, list);
	va_end(list);

	printf(msg);
	exit(-1);
}

u8 myblock[0x4000];
u8 buffer_ipl[1*1024*1024];
u8 buffer_payload[512*1024];

int main()
{
	FILE *i, *o;
	int size, s;
	u32 *code, *p;
	u8 *q;
	
	i = fopen("iplboot.bin", "rb");	
	if (!i)
	{
		ErrorExit("Cannot open iplboot.bin\n");
	}

	memset(myblock, 0, 0x4000);
	memcpy(myblock, nem_exploit, 0x100);
	fread(myblock+0x100, 1, 0xF00, i);

	fclose(i);

	i = fopen("patch.bin", "rb");
	if (!i)
	{
		ErrorExit("Cannot open patch.bin\n");
	}

	fread(myblock+0x1000, 1, 0x3000, i);
	fclose(i);

	i = fopen("ipl.bin", "rb");
	if (!i)
	{
		ErrorExit("Cannot open iplboot.bin\n");
	}	

	size = fread(buffer_ipl, 1, sizeof(buffer_ipl), i);
	fclose(i);

	i = fopen("payload.bin", "rb");
	if (!i)
	{
		ErrorExit("Cannot open payload.bin\n");
	}	

	memset(buffer_payload, 0, sizeof(buffer_payload));
	fread(buffer_payload, 1, sizeof(buffer_payload), i);
	fclose(i);

	o = fopen("ipl_result.bin", "wb");
	if (!o)
	{
		ErrorExit("Cannot create ipl_result.bin\n");
	}

	fwrite(myblock, 1, 0x4000, o);
	
	code = (u32 *)(buffer_ipl+0x10);
	
	code[0xE0/4] = 0x09038000; // j 0x040e0000
	code[0xE4/4] = 0; // nop

	p = (u32 *)(buffer_ipl+0x10B10);	
	memcpy(p, buffer_payload, 0x450);
	p = (u32 *)(buffer_ipl+0x11000);
	q = buffer_payload+0x450;
	
	do
	{
		s = p[1];

		memcpy(p+(0x10/4), q, s);
		p += (0x1000/4);
		q += s;

	} while (s != 0x710);


	fwrite(buffer_ipl, 1, size, o);		
	fclose(o);

	printf("Done.\n");

	
	return 0;
}

